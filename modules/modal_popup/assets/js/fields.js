		for( var id in this.fields )
		{
			var field_name = this.fields[id]['name'];
			var field_group = this.fields[id]['group'];
			if( group != 'all' && ( ( isArr == true && group.indexOf(field_group) == -1 ) || ( isArr == false && group != field_group ) ) )
			{
				continue;
			}

			
			list[ id ] = this.fields[id];
		}
		return list;
